# Program to find sum of even numbers between 100 and 200
ctr=100  #Taking counter value 100
sum=0    #Initializing sum to 0 
while ctr<=200:   #While statement to limit counter till 200
    sum=sum+ctr   #Initial ctr value gets added to sum and then the incremented values
    ctr=ctr+2     #Counter is incremented by 2 for even numbers
print("The sum of even numbers between 100 and 200 is",sum) #Final print statement



